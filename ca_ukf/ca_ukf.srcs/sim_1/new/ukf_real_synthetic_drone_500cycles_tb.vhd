            write(output_line, string'("y_vel=") & integer'image(to_integer(y_vel_current)) & string'(" "));
            write(output_line, string'("y_acc=") & integer'image(to_integer(y_acc_current)) & string'(" "));
            write(output_line, string'("z_pos=") & integer'image(to_integer(z_pos_current)) & string'(" "));
            write(output_line, string'("z_vel=") & integer'image(to_integer(z_vel_current)) & string'(" "));
            write(output_line, string'("z_acc=") & integer'image(to_integer(z_acc_current)));
            writeline(output_file, output_line);

            if (cycle mod 50) = 0 then
                report "Completed cycle " & integer'image(cycle) & " / 500";
            end if;

            wait for 10 ns;
        end loop;

        write(output_line, string'(""));
        writeline(output_file, output_line);
        write(output_line, string'("=== Simulation Complete ==="));
        writeline(output_file, output_line);
        write(output_line, string'("Total cycles: 500"));
        writeline(output_file, output_line);

        file_close(output_file);

        report "SIMULATION COMPLETE: 500 cycles processed";
        report "Output file: vhdl_output_synthetic_drone_500cycles.txt";

        sim_done <= true;
        wait;
    end process;

