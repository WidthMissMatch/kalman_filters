    stim_proc : process
        file output_file : text;
        variable output_line : line;
    begin

        file_open(output_file, "vhdl_output_synthetic_drone_500cycles.txt", write_mode);

        reset <= '1';
        start <= '0';
        wait for 100 ns;
        reset <= '0';
        wait for 100 ns;

        write(output_line, string'("=== UKF Simulation: synthetic_drone_500cycles ==="));
        writeline(output_file, output_line);
        write(output_line, string'("Cycles: 500"));
        writeline(output_file, output_line);
        write(output_line, string'(""));
        writeline(output_file, output_line);

        for cycle in 0 to 499 loop
            cycle_count <= cycle;

            z_x_meas <= meas_x_data(cycle);
            z_y_meas <= meas_y_data(cycle);
            z_z_meas <= meas_z_data(cycle);

            start <= '1';
            wait until rising_edge(clk);
            start <= '0';

            wait until done = '1' or cycle_count > 10000;

            if done /= '1' then
                write(output_line, string'("ERROR: Cycle ") & integer'image(cycle) & string'(" timeout!"));
                writeline(output_file, output_line);
                exit;
            end if;

            write(output_line, string'("Cycle ") & integer'image(cycle) & string'(": "));
            write(output_line, string'("x_pos=") & integer'image(to_integer(x_pos_current)) & string'(" "));
            write(output_line, string'("x_vel=") & integer'image(to_integer(x_vel_current)) & string'(" "));
