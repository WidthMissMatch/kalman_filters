def load_dataset(dataset_name):
    """Load real-world dataset CSV"""
    csv_file = DATA_DIR / f"{dataset_name}.csv"
    if not csv_file.exists():
        raise FileNotFoundError(f"Dataset not found: {csv_file}")
    
    df = pd.read_csv(csv_file)
    print(f"Loaded {len(df)} cycles from {dataset_name}")
    
    return df

def format_vhdl_array(values, indent="        "):
    """Format array of Q24.24 values for VHDL"""
    lines = []
    MAX_INT = 2**31 - 1  # Maximum VHDL integer value
    MIN_INT = -(2**31)   # Minimum VHDL integer value

    for i, val in enumerate(values):
        int_val = int(val)

        # Check if value exceeds VHDL integer range
        if int_val > MAX_INT or int_val < MIN_INT:
            # Use hexadecimal bit string literal for large values
            # Convert to 48-bit two's complement
            if int_val < 0:
                int_val = (1 << 48) + int_val  # Two's complement for negative

            hex_str = f'{int_val:012X}'  # 48 bits = 12 hex digits
            line = f'{indent}{i} => signed\'(X"{hex_str}")'
        else:
            # Use to_signed for values within integer range
            line = f'{indent}{i} => to_signed({int_val}, 48)'

        if i < len(values) - 1:
            line += ','

        lines.append(line)

    return '\n'.join(lines)

def generate_testbench(dataset_name, output_file=None):
    """Generate VHDL testbench for dataset"""
    print(f"\n{'='*80}")
    print(f"Generating testbench for: {dataset_name}")
    print('='*80)
    
    # Load dataset
    df = load_dataset(dataset_name)
    
    # Create safe name for VHDL identifiers
    safe_name = dataset_name.replace('-', '_').replace('.', '_')
    
    # Extract measurements (Q24.24 format)
    meas_x_q24 = df['meas_x_q24'].values
    meas_y_q24 = df['meas_y_q24'].values
    meas_z_q24 = df['meas_z_q24'].values
    
    # Format arrays
    print("Formatting measurement arrays...")
    meas_x_array = format_vhdl_array(meas_x_q24)
    meas_y_array = format_vhdl_array(meas_y_q24)
    meas_z_array = format_vhdl_array(meas_z_q24)
    
    # Fill template
    print("Generating VHDL code...")
    vhdl_code = TESTBENCH_TEMPLATE.format(
        dataset_name=dataset_name,
        safe_name=safe_name,
        num_cycles=len(df),
        num_cycles_minus_1=len(df) - 1,
        meas_x_array=meas_x_array,
        meas_y_array=meas_y_array,
        meas_z_array=meas_z_array
    )
    
    # Save testbench
    if output_file is None:
        output_file = TB_OUTPUT_DIR / f"ukf_real_{safe_name}_tb.vhd"
    
    output_file.parent.mkdir(parents=True, exist_ok=True)
    
    with open(output_file, 'w') as f:
        f.write(vhdl_code)
    
    print(f"✓ Testbench saved: {output_file}")
    print(f"  Lines of code: {len(vhdl_code.splitlines())}")
    print(f"  Measurement cycles: {len(df)}")
    
    return output_file

def main():
    parser = argparse.ArgumentParser(description='Generate VHDL Testbenches with File Output')
    parser.add_argument('--dataset', type=str, help='Specific dataset name')
    parser.add_argument('--all', action='store_true', help='Generate for all datasets')
    args = parser.parse_args()
    
    print("="*80)
    print("VHDL TESTBENCH GENERATOR WITH FILE OUTPUT")
    print("="*80)
    
    if args.all:
        # Find all datasets
        datasets = sorted(DATA_DIR.glob("*.csv"))
        real_world = [d.stem for d in datasets if 'drone_euroc' in d.name or 'f1_' in d.name or 'synthetic' in d.name]

        if not real_world:
            print("\n✗ No datasets found")
            return
        
        print(f"Generating testbenches for {len(real_world)} datasets...")
        
        for dataset_name in real_world:
            try:
                generate_testbench(dataset_name)
            except Exception as e:
                print(f"\n✗ Error generating testbench for {dataset_name}: {e}")
    
    elif args.dataset:
        try:
            generate_testbench(args.dataset)
        except Exception as e:
            print(f"\n✗ Error: {e}")
            import traceback
            traceback.print_exc()
    
    else:
        print("\n✗ Please specify --dataset NAME or --all")
        return
    
    print(f"\n{'='*80}")
    print("TESTBENCH GENERATION COMPLETE")
    print('='*80)
    print(f"Output directory: {TB_OUTPUT_DIR}")
    print("\nGenerated testbenches include:")
    print("  - Q24.24 measurements from real datasets")
    print("  - File output for manual inspection")
    print("  - Progress reporting")
    print("  - Timeout protection")
    print("\nNext steps:")
    print("  1. Add testbench to Vivado simulation sources")
    print("  2. Run with GHDL: ghdl -r <testbench_name>")
    print("  3. Run with Vivado: vivado -mode batch -source run_vivado_simulation.tcl")

if __name__ == "__main__":
    main()
