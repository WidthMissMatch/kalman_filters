      write(L, string'(" p_si=0x"));
      hwrite48(L, p_si);
      write(L, string'(" p_bi=0x"));
      hwrite48(L, p_bi);
      writeline(out_file, L);

      wait for CLK_PERIOD * 5;
    end loop;

    report "IMM simulation complete after " & integer'image(N_CYCLES) & " cycles";
    wait;
  end process;

end Behavioral;
